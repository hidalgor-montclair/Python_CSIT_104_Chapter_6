# This function is called get_face.
# It creates and returns a funny "face" made with characters.
def get_face():
    # The variable face_char stores the characters that make a face.
    face_char = 'o    o\n o\n o\nooooo'
    # The function returns the "face" so we can use it later.
    return face_char

# This line prints the text "Say cheese!" to the screen.
print('Say cheese!')

# This line calls the get_face() function, which returns the face, 
# and then prints the face to the screen.
print(get_face())

